public class LoopDenemeleri_For {
    public static void main(String[] args) {
        System.out.println("for döngüsü 1==>9");
        for (int i =1; i<10; i++){
            System.out.println(i);
        }
        System.out.println("for döngüsü 1==>10");

        for (int i =1; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("for döngüsü tek sayilar");
        for (int i =1; i<10; i+=2){
            System.out.println(i);
        }
        System.out.println("for döngüsü çift sayilar");
        for (int i =0; i<=10; i+=2){
            System.out.println(i);
        }
        System.out.println("Döngü bitti :)");

    }
}
